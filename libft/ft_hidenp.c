#include "libft.h"

void	ft_hidenp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!s1[i])
		ft_putchar('1');
	while (s1[i])
	{
		if (s1[i] == s2[j])
			i++;
		if (s1[i] == '\0')
		{
			ft_putchar('1');
			return ;
		}
		if (s2[j] == '\0')
		{
			ft_putchar('0');
			return ;
		}
		j++;
	}
}
